import turtle

def draw_iron_man_face():
    window = turtle.Screen()
    window.bgcolor("#b3d9ff")

    t = turtle.Turtle()
    t.speed(2)
    t.penup()
    t.goto(-100,100)
    t.pendown()

    # Head
    t.color("#ff3333")
    t.begin_fill()
    t.circle(100)
    t.end_fill()

    # Faceplate
    t.color("#ffffff")
    t.penup()
    t.goto(-70, 70)
    t.pendown()
    t.begin_fill()
    t.circle(70)
    t.end_fill()

    # Eyes
    t.color("#000000")
    t.penup()
    t.goto(-55, 95)
    t.pendown()
    t.begin_fill()
    t.circle(10)
    t.end_fill()

    t.penup()
    t.goto(35, 95)
    t.pendown()
    t.begin_fill()
    t.circle(10)
    t.end_fill()

    # Mouth
    t.penup()
    t.goto(-5, 55)
    t.pendown()
    t.color("#000000")
    t.pensize(5)
    t.right(90)
    t.forward(20)

    t.penup()
    t.goto(-100, 100)
    t.pendown()
